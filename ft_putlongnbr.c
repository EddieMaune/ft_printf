#include "ft_printf.h"

void	ft_putlongnbr(long long n)
{
	char c;

	if (n == -9223372036854775807)
	{
		ft_putstr("-9223372036854775807");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		c = n + 48;
		ft_putchar(c);
	}
}
